// =============================================================================
// Merged response — what the frontend ultimately consumes.
// Same shape as before the hot/deep split, for backward compatibility.
// =============================================================================

export const AnalyzeResponseSchema = z.object({
  score: z.number().min(0).max(100),
  verdict: z.enum(['Low', 'Medium', 'High']),
  confidence: z.object({
    score: z.number().min(0).max(100),
    reason: z.string(),
  }),
  breakdown: z.object({
    keyword_match: z.number().min(0).max(100),
    tech_stack_fit: z.number().min(0).max(100),
    experience_level: z.number().min(0).max(100),
    github_signal: z.number().min(0).max(100).nullable(),
    linkedin_signal: z.number().min(0).max(100).nullable(),
  }),
  ats_simulation: z.object({
    would_pass: z.boolean(),
    score: z.number().min(0).max(100),
    threshold: z.number().min(0).max(100),
    reason: z.string(),
    critical_missing_keywords: z.array(AtsCriticalMissingKeywordSchema).optional(),
  }),
  seniority_analysis: z.object({
    expected: z.string(),
    detected: z.string(),
    gap: z.string(),
    strength: z.string(),
    fix: FixSchema.optional(),
  }),
  cv_tone: z.object({
    detected: z.enum(['passive', 'active', 'mixed']),
    // Tool cap is 6 — keep one slot of headroom so an off-by-one from the
    // model never fails an expensive analysis.
    examples: z.array(z.string()).max(8),
    fix: FixSchema.optional(),
  }),
  audit: z.object({
    cv: z.object({
      score: z.number().min(0).max(100),
      issues: z.array(
        IssueHotSchema.extend({ fix: FixSchema.optional() }),
      ),
      strengths: z.array(z.string()).optional(),
    }),
    // github / linkedin are SPARSE sources: when the data is thin or absent,
    // Claude occasionally returns a bare string (e.g. "No GitHub profile to
    // audit") instead of the object. `.catch` coerces any malformed value to an
    // empty audit so a secondary field never fails the whole (expensive,
    // ~3 min) analysis. cv / jd_match stay strict — they're always present.
    github: z
      .object({
        score: z.number().min(0).max(100).nullable(),
        issues: z.array(
          IssueHotSchema.extend({ fix: FixSchema.optional() }),
        ),
        strengths: z.array(z.string()).optional(),
      })
      .catch({ score: null, issues: [], strengths: [] }),
    linkedin: z
      .object({
        score: z.number().min(0).max(100).nullable(),
        issues: z.array(
          IssueHotSchema.extend({ fix: FixSchema.optional() }),
        ),
        strengths: z.array(z.string()).optional(),
      })
      .catch({ score: null, issues: [], strengths: [] }),
    // P1: cover / motivation letter audit. Optional (absent on lean/owner
    // audits and on rows stored before P1); score is null when no letter.
    cover_letter: z
      .object({
        score: z.number().min(0).max(100).nullable(),
        issues: z.array(IssueHotSchema.extend({ fix: FixSchema.optional() })),
        strengths: z.array(z.string()).optional(),
      })
      .catch({ score: null, issues: [], strengths: [] })
      .optional(),
    jd_match: AuditJdMatchSchema,
  }),
  hidden_red_flags: z.array(
    z.object({
      flag: z.string(),
      perception: z.string(),
      fix: FixSchema.optional(),
    }),
  ),
  correlation: z
    .object({
      detected: z.boolean(),
      explanation: z.string(),
    })
    .optional(),
  job_details: JobDetailsSchema,
  project_recommendation: ProjectRecommendationSchema.optional(),
  challenge_analysis: ChallengeAnalysisSchema.optional(),
  technical_analysis: TechnicalAnalysisSchema.optional(),
  negotiation_analysis: NegotiationAnalysisSchema.nullable().optional(),
  /**
   * Pre-computed cross-profile inconsistencies from the user's ProfileDigest.
   * Surfaced in the analysis result so the frontend can render a dedicated
   * "Consistency check" section without re-fetching the digest.
   * Empty / undefined for anonymous users (no digest).
   */
  highlight_terms: HighlightTermsSchema.optional(),
  // Bullet-by-bullet review — optional so pre-densification DB rows replay.
  bullet_reviews: BulletReviewsSchema.optional(),
  // Set by the analysis shaper on redacted (free) payloads only.
  premium_locked: PremiumLockedSchema.optional(),
  cross_profile_inconsistencies: z
    .array(CrossProfileInconsistencySchema)
    .optional(),
  /**
   * Per-source chronology used by the Consistency tab timeline visualization.
   * Each entry is one job from one source — same job across multiple sources
   * appears multiple times (one per source) so the frontend can render parallel
   * bars and surface date divergences spatially.
   */
  timeline_entries: z.array(TimelineEntrySchema).optional(),
});

export class AnalyzeResponseDto extends createZodDto(AnalyzeResponseSchema) {}
export type AnalyzeResponse = z.infer<typeof AnalyzeResponseSchema>;
export type HotAnalyzeResponse = z.infer<typeof HotAnalyzeResponseSchema>;
export type DeepAnalyzeResponse = z.infer<typeof DeepAnalyzeResponseSchema>;
export type Issue = z.infer<typeof IssueSchema>;
export type IssueHot = z.infer<typeof IssueHotSchema>;
export type Fix = z.infer<typeof FixSchema>;

/**
 * Attach positional deep `fix` blocks onto their owning hot issues.
 *
 * The deep pass returns fixes as flat arrays that MUST be the same length and
 * order as the hot issues (enforced only by the prompt). If the model returns a
 * mismatched length, attaching by index would silently bind the WRONG fix to an
 * issue — the exact trust defect we must avoid. So on any length mismatch we
 * drop the fixes for that section (a missing fix degrades gracefully to a
 * skeleton; a wrong fix does not) and log a warning for observability.
 */
export function attachFixes<I, F>(
  issues: readonly I[],
  fixes: readonly F[] | undefined,
  label: string,
): (I & { fix: F | undefined })[] {
  if (!fixes) {
    return issues.map((issue) => ({ ...issue, fix: undefined }));
  }
  if (fixes.length !== issues.length) {
    // eslint-disable-next-line no-console
    console.warn(
      `[MERGE_ALIGN] ${label}: deep fixes length ${fixes.length} != hot issues length ${issues.length} — dropping fixes for this section to avoid misattachment`,
    );
    return issues.map((issue) => ({ ...issue, fix: undefined }));
  }
  return issues.map((issue, i) => ({ ...issue, fix: fixes[i] }));
}

/**
 * Merge hot pass + deep pass into the full AnalyzeResponse shape that the
 * frontend consumes. Re-injects `fix` blocks into their owners positionally,
 * guarding against length mismatches (see `attachFixes`).
 *
 * If `deep` is null/undefined, returns the hot result with deep fields absent
 * — the frontend handles missing deep fields with skeleton placeholders.
 */
export function mergeHotAndDeep(
  hot: HotAnalyzeResponse,
  deep: DeepAnalyzeResponse | null | undefined,
): AnalyzeResponse {
  // `cross_profile_inconsistencies` and `timeline_entries` aren't part of
  // HotAnalyzeResponseSchema — they're appended onto the result by
  // AnalyzeCvUseCase after this merge runs (sourced from the user's
  // ProfileDigest). When GetAnalysisUseCase re-runs this merge on a stored
  // row, the fields are present on the in-memory object even though the Zod
  // type doesn't list them. Preserve them through the merge so reloading an
  // analysis doesn't drop the Consistency tab data.
  const passthroughInconsistencies = (
    hot as unknown as { cross_profile_inconsistencies?: unknown }
  ).cross_profile_inconsistencies as
    | AnalyzeResponse['cross_profile_inconsistencies']
    | undefined;
  const passthroughTimeline = (
    hot as unknown as { timeline_entries?: unknown }
  ).timeline_entries as AnalyzeResponse['timeline_entries'] | undefined;

  const merged: AnalyzeResponse = {
    score: hot.score,
    verdict: hot.verdict,
    confidence: hot.confidence,
    breakdown: hot.breakdown,
    ats_simulation: {
      ...hot.ats_simulation,
      // Leave undefined when deep hasn't arrived — the frontend uses this to
      // distinguish "loading the keyword list" from "no missing keywords".
      critical_missing_keywords: deep?.ats_critical_missing_keywords,
    },
    seniority_analysis: {
      ...hot.seniority_analysis,
      fix: deep?.fixes.seniority_analysis,
    },
    cv_tone: {
      ...hot.cv_tone,
      fix: deep?.fixes.cv_tone,
    },
    audit: {
      cv: {
        ...hot.audit.cv,
        issues: attachFixes(
          hot.audit.cv.issues,
          deep?.fixes.audit_cv_issues,
          'audit_cv_issues',
        ),
      },
      github: {
        ...hot.audit.github,
        issues: attachFixes(
          hot.audit.github.issues,
          deep?.fixes.audit_github_issues,
          'audit_github_issues',
        ),
      },
      linkedin: {
        ...hot.audit.linkedin,
        issues: attachFixes(
          hot.audit.linkedin.issues,
          deep?.fixes.audit_linkedin_issues,
          'audit_linkedin_issues',
        ),
      },
      jd_match: hot.audit.jd_match,
    },
    hidden_red_flags: attachFixes(
      hot.hidden_red_flags,
      deep?.fixes.hidden_red_flags,
      'hidden_red_flags',
    ),
    // correlation is optional now — only present on legacy analyses.
    correlation: hot.correlation,
    job_details: hot.job_details,
    challenge_analysis: hot.challenge_analysis,
    project_recommendation: deep?.project_recommendation,
    // highlight_terms is generated by the deep pass in the split flow.
    highlight_terms: deep?.highlight_terms,
    // technical_analysis lives in the hot pass for new analyses; fall back to
    // deep for analyses created before the move (mid-format DB rows).
    technical_analysis: hot.technical_analysis ?? deep?.technical_analysis,
  };
  if (passthroughInconsistencies && passthroughInconsistencies.length > 0) {
    merged.cross_profile_inconsistencies = passthroughInconsistencies;
  }
  if (passthroughTimeline && passthroughTimeline.length > 0) {
    merged.timeline_entries = passthroughTimeline;
  }
  return merged;
}

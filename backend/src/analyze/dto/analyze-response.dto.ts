export const FixSchema = z.object({
  summary: z.string(),
  steps: z.array(z.string()).min(1).max(5),
  example: z
    .object({
      before: z.string(),
      after: z.string(),
    })
    .nullable(),
  project_idea: z
    .object({
      name: z.string(),
      description: z.string(),
      endpoints: z.array(z.string()),
      bonus: z.string().nullable(),
      proves: z.string(),
    })
    .nullable(),
  time_required: z.string(),
});

// Issue without fix — used in the hot pass; fix is filled in from deep pass
// at merge time.
export const IssueHotSchema = z.object({
  // Stable per-issue key (concurrency-audit rule R2). Assigned deterministically
  // in the hot pass (slug of scope + hash of `what`), NOT generated by Claude.
  // Per-fix generation and storage key off this — never off array position.
  // Optional for back-compat: analyses stored before the split lack it.
  id: z.string().optional(),
  severity: z.enum(['critical', 'major', 'minor']).catch('minor'),
  category: z
    .enum([
      'keywords',
      'impact',
      'seniority',
      'stack',
      'format',
      'tone',
      'consistency',
    ])
    .catch('format'),
  what: z.string(),
  why: z.string(),
});

// Full Issue (hot + fix). Used in the merged AnalyzeResponse.
export const IssueSchema = IssueHotSchema.extend({
  fix: FixSchema,
});

// Claude sometimes returns a string instead of an array — coerce gracefully
const strOrArr = z
  .union([z.array(z.string()), z.string()])
  .transform((v) => (Array.isArray(v) ? v : [v]));

const techItemSchema = z.union([
  z.string(),
  z.object({ name: z.string(), category: z.string(), reason: z.string() }),
]);

